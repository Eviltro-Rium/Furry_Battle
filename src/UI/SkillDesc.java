    static String getAttackDesc(GameCharacter ch, Card card) {
        String name = ch.getName().replace("AI ", "");
        String[] descs = ATTACK.get(name);
        if (descs == null) return "";
        if (card.isItemCard()) return getItemDesc(card);
        int v = card.getValue();
        if (v >= 0 && v <= 7) return descs[v == 0 ? 7 : v - 1];
        return "";
    }

    static String getDefendDesc(GameCharacter ch, Card card) {
        String name = ch.getName().replace("AI ", "");
        String[] descs = DEFEND.get(name);
        if (descs == null) return "";
        if (card.isItemCard()) return getItemDesc(card);
        int v = card.getValue();
        if (v == 0) return descs[3];
        if (v >= 1 && v <= 3) return descs[v - 1];
        if (v >= 4 && v <= 7) return "无防御效果";
        return "";
    }

    private static String getItemDesc(Card card) {
        if (card.isBlack()) {
            if (card.isShuffleToDeck()) return "指定颜色，弃牌库除顶牌外洗入牌库，可搭桥继续出牌";
            if (card.isDrawTwo()) return "指定颜色，抽2张牌，可搭桥继续出牌";
            return "指定颜色，可搭桥继续出牌";
        }
        if (card.isPotion()) return "恢复5[生命]，可搭桥继续出牌";
        if (card.isDrawThree()) return "抽3张牌，可搭桥继续出牌";
        if (card.isSwapHand()) return "交换双方所有手牌，可搭桥继续出牌";
        if (card.isSuperPurify()) return "清除自身所有debuff（[灼烧]/[冷冻]/[流血]），可搭桥继续出牌";
        if (card.isPurify()) return "净化1层debuff（[灼烧]/[冷冻]/[流血]），可搭桥继续出牌";
        return "";
    }
